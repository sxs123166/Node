// 1.什么是包？
// Node.js中的第三方模块又叫做包

// 2.从哪里下载包？
// 国外有一家IT公司，叫做npm,Inc。这家公司旗下有一个非常著名的网址https://www.npmjs.com/,它是全球
// 最大的包共享平台。
// npm, Inc公司提供了一个地址为https://registry.npmjs.org/的服务器，来对外共享所有的包，我们可以从这个服务器上
// 下载所有的包

// 注意：
// 从https://www.npmjs.com/网站上搜索自己所需要的包
// 从https://registry.npmjs.org/服务器上下载自己所需要的包

// 3.如何下载包？
// npm,Inc.公司提供了一个包管理工具，我们可以使用这个包管理工具，从https://registry.npmjs.org/服务器把
// 需要的包下载到本地使用

// 这个包管理工具的名字叫做Node Package Manager(简称npm包管理工具)，这个包管理工具随着Node.js
// 的安装包一起被安装到了用户的电脑上

// 可以在终端中执行npm-v命令，查看自己npm包管理工具的版本号

